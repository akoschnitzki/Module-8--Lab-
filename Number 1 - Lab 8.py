# In this problem, you can be able to input two numbers
# and it will let you know if they are equal to each other
# or if they are not equal to each other.

def test_number(x, y):
   if x == y:
       return True
   else:
       return False

print(test_number(7, 2))
print(test_number(3, 2))
print(test_number(2, 2))
print(test_number(7, 3))
print(test_number(8, 8))
